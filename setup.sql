CREATE TABLE `USERS`(
	`ID` INT(11) NOT NULL AUTO_INCREMENT,
	`USER` VARCHAR(32),
	`MAIL` VARCHAR(32),
	`PASSWORD` VARCHAR(64),
	`TYPE` INT(11),
	PRIMARY KEY (`ID`),
	UNIQUE (`USER`,`MAIL`),
	CHECK (TYPE BETWEEN 0 AND 1)
);

CREATE TABLE `CATEGORIES`(
	`ID` INT(11) NOT NULL AUTO_INCREMENT,
	`PARENTID` INT(11),
	`TITLE` VARCHAR(32),
	PRIMARY KEY (`ID`),
	UNIQUE (`TITLE`)
);

CREATE TABLE `ARTICLES`(
	`ID` INT(11) NOT NULL AUTO_INCREMENT,
	`TITLE` VARCHAR(96),
	`TYPE` INT(11),
	`CATEGORIES` INT(11),
	`DATE` DATE,
	`CONTENT`	TEXT,
	`IMAGEHEADER` TEXT
	PRIMARY KEY (`ID`),
	UNIQUE (`TITLE`),
	CHECK (TYPE BETWEEN 0 AND 2)
);

